from turtle import *

def o() :#function for letter o
    penup()
    forward(25)
    pendown()
    circle(25)
    penup()
    
def e() :#function for letter e
    penup()
    forward(25)
    circle(25,270)
    pendown()
    left(90)
    forward(50)
    left(90)
    circle(25,315)
    penup()

def u() :#function for letter u
    penup()
    left(90)
    forward(50)
    pendown()
    right(180)
    forward(25)
    circle(25,180)
    forward(25)
    right(180)
    forward(50)
    penup()

def a() : #function for letter a
    penup()
    forward(25)
    pendown()
    circle(25,360)
    penup()
    forward(25)
    pendown()
    left(90)
    forward(50)
    penup()

def i() : #function for letter i
    forward(25)
    left(90)
    pendown()
    forward(50)
    penup()
    forward(25)
    pendown()
    forward(1)
    penup()






